/// Spacing system for Digital Cassette
/// Base unit: 4px
class AppSpacing {
  AppSpacing._();

  static const double xs = 4.0; // 4px - Tight spacing
  static const double sm = 8.0; // 8px - Small spacing
  static const double md = 12.0; // 12px - Medium spacing
  static const double lg = 16.0; // 16px - Standard spacing
  static const double xl = 24.0; // 24px - Large spacing
  static const double xxl = 32.0; // 32px - XL spacing
  static const double xxxl = 48.0; // 48px - XXL spacing

  // Screen padding
  static const double screenHorizontal = 16.0;
  static const double screenVertical = 16.0;

  // Card spacing
  static const double cardPadding = 12.0;
  static const double cardSpacing = 12.0;

  // Component sizing
  static const double buttonHeight = 48.0;
  static const double inputHeight = 48.0;
  static const double bottomNavHeight = 56.0;
  static const double appBarHeight = 56.0;

  // Border radius
  static const double radiusSmall = 8.0;
  static const double radiusMedium = 12.0;
  static const double radiusLarge = 20.0;
  static const double radiusXLarge = 24.0;
  static const double radiusCircular = 9999.0;

  // Icon sizes
  static const double iconSmall = 16.0;
  static const double iconMedium = 24.0;
  static const double iconLarge = 32.0;
}
